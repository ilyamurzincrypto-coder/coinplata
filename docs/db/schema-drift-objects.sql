CREATE TABLE IF NOT EXISTS public.rapira_alert_state (
  pair        text        NOT NULL PRIMARY KEY,
  ref_mid     numeric,
  ref_at      timestamptz,
  updated_at  timestamptz NOT NULL DEFAULT now()
);

-- ─────────────────────────────────────────────────────────────────────────────
-- special_rates — СПЕЦ-КУРСЫ (НЕРЕЗ/СБП и т.п.). Полностью перезаписывается через
-- replace_special_rates(jsonb). kind/side/settle — категоризация; value — курс.
-- ─────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.special_rates (
  id            uuid        NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
  kind          text        NOT NULL,
  pair          text,
  side          text,
  settle        text,
  from_currency text,
  to_currency   text,
  value         numeric     NOT NULL,
  imported_at   timestamptz NOT NULL DEFAULT now(),
  updated_by    uuid
);

-- ─────────────────────────────────────────────────────────────────────────────
-- pairs — дрейф-КОЛОНКИ (сама таблица описана в store/rates.jsx; здесь только то,
-- чего нет в коде: маржинальная модель market_rate + buy/sell margin, is_master,
-- reverse_locked). rate = market_rate + buy_margin (см. set_pair_margins).
-- ─────────────────────────────────────────────────────────────────────────────
ALTER TABLE public.pairs ADD COLUMN IF NOT EXISTS market_rate    numeric;
ALTER TABLE public.pairs ADD COLUMN IF NOT EXISTS buy_margin     numeric NOT NULL DEFAULT 0;
ALTER TABLE public.pairs ADD COLUMN IF NOT EXISTS sell_margin    numeric NOT NULL DEFAULT 0;
ALTER TABLE public.pairs ADD COLUMN IF NOT EXISTS is_master      boolean NOT NULL DEFAULT false;
ALTER TABLE public.pairs ADD COLUMN IF NOT EXISTS reverse_locked boolean NOT NULL DEFAULT false;

-- Единая точка записи маржи в default-пару: rate = market + buy_margin.
CREATE OR REPLACE FUNCTION public.set_pair_margins(p_from text, p_to text, p_market numeric, p_buy_margin numeric, p_sell_margin numeric DEFAULT NULL::numeric)
 RETURNS void LANGUAGE plpgsql SET search_path TO 'public'
AS $function$
begin
  update public.pairs
     set market_rate = p_market,
         buy_margin  = p_buy_margin,
         sell_margin = coalesce(p_sell_margin, p_buy_margin),
         rate        = p_market + p_buy_margin,
         updated_at  = now()
   where from_currency = p_from and to_currency = p_to and is_default = true;
end $function$;

-- Полная замена special_rates (owner/admin only). Клиент шлёт массив строк.
CREATE OR REPLACE FUNCTION public.replace_special_rates(p_rows jsonb)
 RETURNS void LANGUAGE plpgsql SECURITY DEFINER SET search_path TO 'public'
AS $function$
declare v_role text;
begin
  select role into v_role from public.users where id = auth.uid();
  if v_role not in ('owner','admin') then
    raise exception 'Only owner/admin can set special rates' using errcode = '42501';
  end if;

  delete from public.special_rates;

  insert into public.special_rates(kind, pair, side, settle, from_currency, to_currency, value, updated_by, imported_at)
  select coalesce(e->>'kind',''), e->>'pair', e->>'side', e->>'settle',
         e->>'from', e->>'to', (e->>'value')::numeric, auth.uid(), now()
  from jsonb_array_elements(coalesce(p_rows, '[]'::jsonb)) as e
  where (e->>'value') is not null;
end;
$function$;

