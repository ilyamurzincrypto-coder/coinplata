-- ─────────────────────────────────────────────────────────────────────────────
-- external_rates — сырьё внешних котировок (Rapira, Tolunay, …). Одна строка на
-- (source, pair, fetched_at). v_external_rates_latest берёт последнюю по паре.
-- ─────────────────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.external_rates (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, -- прод: nextval('external_rates_id_seq')
  source      text        NOT NULL,
  pair        text        NOT NULL,
  bid         numeric,
  ask         numeric,
  mid         numeric,
  raw         jsonb,
  fetched_at  timestamptz NOT NULL DEFAULT now(),
  created_at  timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS external_rates_fetched_at_idx ON public.external_rates USING btree (fetched_at DESC);
CREATE INDEX IF NOT EXISTS external_rates_source_pair_idx ON public.external_rates USING btree (source, pair, fetched_at DESC);

-- Последняя котировка на (source, pair).
CREATE OR REPLACE VIEW public.v_external_rates_latest AS
  SELECT DISTINCT ON (source, pair) source, pair, bid, ask, mid, fetched_at
    FROM public.external_rates
   ORDER BY source, pair, fetched_at DESC;

-- ─────────────────────────────────────────────────────────────────────────────
